;************************************************************************
;	セクタ読み込み(LBA指定)
;------------------------------------------------------------------------
;	事前にドライブパラメータを取得しておく
;========================================================================
;■書式		: WORD read_lba(drive, lba, sect, dst);
;
;■引数
;	drive	: drive構造体のアドレス
;			:（ドライブパラメータが格納されている）
;	lba		: LBA
;	sect	: 読み出しセクタ数
;	dst		: 読み出し先アドレス
;
;■戻り値	: 読み込んだセクタ数
;************************************************************************
read_lba:
		;---------------------------------------
		; 【スタックフレームの構築】
		;---------------------------------------
												; ------|--------
												;    +10| コピー先
												;    + 8| セクタ数
												;    + 6| LBA（2バイト）
												;    + 4| ドライブ情報
												; ------+--------
												;    + 2| IP（戻り番地）
		push	bp								;  BP+ 0| BP（元の値）
		mov		bp, sp							; ------+--------
