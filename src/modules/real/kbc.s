;************************************************************************
;	KBCの出力バッファに書き込む
;========================================================================
;■書式		: WORD KBC_Data_Write(data);
;
;■引数
;	data	: 書き込みデータ
;
;■戻り値	: 成功(0以外)、失敗(0)
;************************************************************************
KBC_Data_Write:
                ;---------------------------------------
                ; 【スタックフレームの構築】
                ;---------------------------------------
                                                        ; ------|--------
                                                        ;    + 4| データ
                                                        ;    + 2| IP（戻り番地）
                push	bp								;  BP+ 0| BP（元の値）
                mov		bp, sp							; ------+--------

                ;---------------------------------------
                ; 【レジスタの保存】
                ;---------------------------------------
                push	cx